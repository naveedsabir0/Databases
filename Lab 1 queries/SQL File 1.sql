CREATE DATABASE stockexchange;
use stockexchange;


DROP TABLE IF EXISTS trades CASCADE;
DROP TABLE IF EXISTS broker_stock_ex CASCADE ;
DROP TABLE IF EXISTS shares_prices CASCADE ;
DROP TABLE IF EXISTS shares CASCADE ;
DROP TABLE IF EXISTS stock_exchanges CASCADE ;
DROP TABLE IF EXISTS companies CASCADE ;
DROP TABLE IF EXISTS brokers CASCADE ;
DROP TABLE IF EXISTS currencies CASCADE ;
DROP TABLE IF EXISTS places CASCADE ;

CREATE TABLE places (
  place_id          INT(6),
  city              VARCHAR(50),
  country           VARCHAR(50),
  PRIMARY KEY (place_id)
);

CREATE TABLE currencies (
  currency_id       INT(6),
  symbol            VARCHAR(5),
  name              VARCHAR(50),
 PRIMARY KEY (currency_id)
);

CREATE TABLE brokers (
  broker_id         INT(6),
  first_name        VARCHAR(25),
  last_name         VARCHAR(25),
  PRIMARY KEY (broker_id)
);

CREATE TABLE companies (
  company_id        INT(6),
  name              VARCHAR(20),
  place_id          INT(6),
  PRIMARY KEY (company_id),
  FOREIGN KEY (place_id) REFERENCES places(place_id)
);

CREATE TABLE stock_exchanges (
  stock_ex_id       INT(6),
  name              VARCHAR(50),
  symbol            VARCHAR(10),
  place_id          INT(6),
 PRIMARY KEY (stock_ex_id),
 FOREIGN KEY (place_id) REFERENCES places(place_id)
);

CREATE TABLE broker_stock_ex (
  broker_id         INT(6),
  stock_ex_id       INT(6),
  FOREIGN KEY (broker_id) REFERENCES brokers(broker_id),
 FOREIGN KEY (stock_ex_id) REFERENCES stock_exchanges(stock_ex_id),
  PRIMARY KEY (broker_id, stock_ex_id)
);

CREATE TABLE shares (
  share_id          INT(6),
  company_id        INT(6),
  currency_id       INT(6),
  PRIMARY KEY (share_id),
  FOREIGN KEY (company_id) REFERENCES companies(company_id),
  FOREIGN KEY (currency_id) REFERENCES currencies(currency_id)
);

CREATE TABLE shares_prices (
  share_id          INT(6),
  price             FLOAT(10,4),
  time_start        DATE,
  time_end          DATE,
  PRIMARY KEY (share_id,time_start),
  FOREIGN KEY (share_id) REFERENCES shares(share_id)
);

CREATE TABLE trades (
  trade_id          INT(9),
  share_id          INT(6),
  broker_id         INT(6),
  stock_ex_id       INT(6),
  transaction_time  DATE,
  share_amount      INT(9),
  price_total       FLOAT(20,2),
 PRIMARY KEY (trade_id),
  FOREIGN KEY (share_id) REFERENCES shares(share_id),
  FOREIGN KEY (broker_id) REFERENCES brokers(broker_id),
  FOREIGN KEY (stock_ex_id) REFERENCES stock_exchanges(stock_ex_id)
);

